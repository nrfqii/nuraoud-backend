<script>
document.addEventListener('DOMContentLoaded', function() {
    // Product Categories Chart
    const categoryCanvas = document.getElementById('categoryChart');
    const categoryData = JSON.parse(categoryCanvas.dataset.category);
    const categoryCtx = categoryCanvas.getContext('2d');
    const categoryChart = new Chart(categoryCtx, {
        type: 'doughnut',
        data: {
            labels: ['Oud', 'Perfume', 'Attar', 'Oil'],
            datasets: [{
                data: categoryData,
                backgroundColor: [
                    '#3B82F6',
                    '#10B981',
                    '#F59E0B',
                    '#EF4444'
                ],
                borderWidth: 1
            }]
        },
        options: {
            responsive: true,
            plugins: {
                legend: {
                    position: 'bottom',
                }
            }
        }
    });

    // Order Status Chart
    const statusCanvas = document.getElementById('orderStatusChart');
    const orderStatusData = JSON.parse(statusCanvas.dataset.status);
    const orderStatusCtx = statusCanvas.getContext('2d');
    const orderStatusChart = new Chart(orderStatusCtx, {
        type: 'pie',
        data: {
            labels: ['Waiting', 'Processing', 'Shipped', 'Delivered', 'Cancelled'],
            datasets: [{
                data: orderStatusData,
                backgroundColor: [
                    '#F59E0B',
                    '#3B82F6',
                    '#8B5CF6',
                    '#10B981',
                    '#EF4444'
                ],
                borderWidth: 1
            }]
        },
        options: {
            responsive: true,
            plugins: {
                legend: {
                    position: 'bottom',
                }
            }
        }
    });

    // Monthly Sales Chart
    const salesCanvas = document.getElementById('salesChart');
    const monthlySales = JSON.parse(salesCanvas.dataset.sales);
    const salesCtx = salesCanvas.getContext('2d');
    const salesChart = new Chart(salesCtx, {
        type: 'line',
        data: {
            labels: [
                'January', 'February', 'March', 'April', 'May', 'June',
                'July', 'August', 'September', 'October', 'November', 'December'
            ],
            datasets: [{
                label: 'Monthly Sales',
                data: monthlySales,
                borderColor: '#3B82F6',
                backgroundColor: 'rgba(59, 130, 246, 0.1)',
                tension: 0.4,
                fill: true
            }]
        },
        options: {
            responsive: true,
            scales: {
                y: {
                    beginAtZero: true,
                    ticks: {
                        callback: function(value) {
                            return 'Rp ' + value.toLocaleString('id-ID');
                        }
                    }
                }
            },
            plugins: {
                legend: {
                    display: false
                }
            }
        }
    });
});
</script>
